// Logical Operators are used to perform logical operations, the logical operators are AND &&, OR ||, NOT !

let a = 4;
let b = 6;

console.log(a > 0 && b > 0);
console.log(a > 0 && b < 2);
console.log(a < 0 && b > 2);
console.log(a < 0 && b < 8);

console.log(a > 0 || b > 0);
console.log(a > 0 || b < 2);
console.log(a < 0 || b > 2);
console.log(a < 0 || b < 8);

let yes = true;
let no = false;

console.log(!yes);
console.log(!no);
